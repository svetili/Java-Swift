package replace;

public class Replace {
	private String oldWord;
	private String newWord;
	
	public Replace(String oldWord, String newWord) {
		this.oldWord = oldWord;
		this.newWord = newWord;
	}

	public String getOldWord() {
		return oldWord;
	}

	public String getNewWord() {
		return newWord;
	}
	

}
